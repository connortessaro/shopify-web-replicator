import { mkdir, writeFile } from "node:fs/promises";
import { basename, dirname, join } from "node:path";

import {
  stableCommerceArtifact,
  pageTypeLabels,
  stableThemeArtifacts,
  type GeneratedThemeArtifact,
  type GenerationResult,
  type ReferenceAnalysis,
  type ThemeMapping
} from "@shopify-web-replicator/shared";

type GenerateInput = {
  analysis: ReferenceAnalysis;
  mapping: ThemeMapping;
};

function sectionTypeFromPath(sectionPath: string): string {
  return basename(sectionPath, ".liquid");
}

function schemaNameForPageType(pageType: ReferenceAnalysis["pageType"]): string {
  if (pageType === "homepage") {
    return "Generated homepage";
  }

  if (pageType === "product_page") {
    return "Generated product";
  }

  if (pageType === "collection_page") {
    return "Generated collection";
  }

  return "Generated landing";
}

function renderCommerceWiringSnippet(pageType: ReferenceAnalysis["pageType"]): string {
  const snippetType = basename(stableCommerceArtifact.path, ".liquid");

  return `
    {% render '${snippetType}', page_type: '${pageType}' %}
`;
}

