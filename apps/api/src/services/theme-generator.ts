function renderSection(analysis: ReferenceAnalysis, mapping: ThemeMapping): string {
  const sectionType = sectionTypeFromPath(mapping.sectionPath);

  if (analysis.pageType === "product_page") {
    return renderProductSection(sectionType, analysis, mapping);
  }

  if (analysis.pageType === "collection_page") {
    return renderCollectionSection(sectionType, analysis, mapping);
  }

  return renderMarketingSection(sectionType, analysis, mapping);
}

function renderTemplate(analysis: ReferenceAnalysis, mapping: ThemeMapping): string {
  const sectionType = sectionTypeFromPath(mapping.sectionPath);

  return JSON.stringify(
    {
      sections: {
        main: {
          type: sectionType,
          settings: {
            eyebrow: mapping.title,
            heading: mapping.sections[0]?.heading ?? mapping.title,
            body: mapping.sections[0]?.body ?? mapping.summary,
            mapping_summary: mapping.summary,
            ...(analysis.pageType !== "collection_page"
              ? { cta_label: mapping.sections[0]?.ctaLabel ?? "Review generated output" }
              : {})
          }
        }
      },
      order: ["main"]
    },
    null,
    2
  );
}

export class ShopifyThemeGenerator {
  constructor(readonly themeWorkspacePath: string) {}

  async generate({ analysis, mapping }: GenerateInput): Promise<{
    artifacts: GeneratedThemeArtifact[];
    generation: GenerationResult;
  }> {
    const artifacts = stableThemeArtifacts[analysis.pageType];
    await mkdir(dirname(join(this.themeWorkspacePath, mapping.sectionPath)), { recursive: true });
    await mkdir(dirname(join(this.themeWorkspacePath, mapping.templatePath)), { recursive: true });

    await writeFile(join(this.themeWorkspacePath, mapping.sectionPath), renderSection(analysis, mapping));
    await writeFile(join(this.themeWorkspacePath, mapping.templatePath), renderTemplate(analysis, mapping));

    const generatedAt = new Date().toISOString();

    return {
      artifacts: [
        {
          kind: "section",
          path: mapping.sectionPath,
          status: "generated",
          description: artifacts.sectionDescription,
          lastWrittenAt: generatedAt
        },
        {
          kind: "template",
          path: mapping.templatePath,
          status: "generated",
          description: artifacts.templateDescription,
          lastWrittenAt: generatedAt
        }
      ],
      generation: {
        generatedAt,
        templatePath: mapping.templatePath,
        sectionPath: mapping.sectionPath
      }
    };
  }
}
